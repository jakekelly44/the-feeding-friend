export function getBreedsBySpecies(species: 'dog' | 'cat'): Breed[] {
  return species === 'dog' ? dogBreeds : catBreeds;
}

export function getBreedById(id: string): Breed | undefined {
  return [...dogBreeds, ...catBreeds].find(b => b.id === id);
}

export function isLongHaired(breedId: string | null): boolean {
  if (!breedId) return false;
  const breed = getBreedById(breedId);
  return breed?.coatType === 'long';
}
